// Helper: create a span with inline SVG
export function icon(name, className = '') {
  const svg = Icons[name]
  if (!svg) return ''
  const span = document.createElement('span')
  span.className = `icon ${className}`.trim()
  span.innerHTML = svg
  return span.outerHTML
}
